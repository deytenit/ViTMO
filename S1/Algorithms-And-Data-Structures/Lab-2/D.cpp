void insertion_sort(vector<int>& a) {
    for (int i = 1; i < a.size(); i++) {
        int j = i - 1;
        bool flag = false;
        while (j >= 0 && a[j + 1] < a[j]) {
            swap(a[j + 1], a[j]);
            j--;
            flag = true;
        }
        if (flag) {
            for (int it : a) {
                cout << it << ' ';
            }
            cout << '\n';
        }
    }
}

